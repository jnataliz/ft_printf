#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	size_t	x;
	char	nl;

	x = 0;
	nl = '\n';
	if (s)
	{
		while (s[x] != '\0')
		{
			write(fd, &s[x], 1);
			x++;
		}
		write(fd, &nl, 1);
	}
}
